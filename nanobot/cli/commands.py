@app.command()
def doctor(
    workspace: str | None = typer.Option(None, "--workspace", "-w", help="Workspace directory"),
    config: str | None = typer.Option(None, "--config", "-c", help="Path to config file"),
    output_format: str = typer.Option("markdown", "--format", help="Output format: markdown or json"),
    limit: int = typer.Option(5, "--limit", help="Max items per report section"),
    session_key: str | None = typer.Option(None, "--session-key", help="Include a detailed tail for one session key"),
):
    """Summarize nanobot runtime records for troubleshooting."""
    from nanobot.config.loader import load_config, set_config_path
    from nanobot.debug.runtime_diagnostics import build_report, render_markdown

    config_path = None
    if config:
        config_path = Path(config).expanduser().resolve()
        if not config_path.exists():
            console.print(f"[red]Error: Config file not found: {config_path}[/red]")
            raise typer.Exit(1)
        set_config_path(config_path)

    loaded = load_config(config_path)
    workspace_path = Path(workspace).expanduser() if workspace else loaded.workspace_path
    report = build_report(
        config_path=config_path,
        workspace=workspace_path,
        limit=max(1, limit),
        session_key=session_key,
    )

    if output_format == "json":
        typer.echo(json.dumps(report, indent=2, ensure_ascii=False))
    else:
        console.print(Markdown(render_markdown(report)))


@app.command("retry-cron")
def retry_cron(
    job_id: str = typer.Argument(..., help="Cron job ID to rerun"),
    workspace: str | None = typer.Option(None, "--workspace", "-w", help="Workspace directory"),
    config: str | None = typer.Option(None, "--config", "-c", help="Path to config file"),
    markdown: bool = typer.Option(True, "--markdown/--no-markdown", help="Render assistant output as Markdown"),
    logs: bool = typer.Option(False, "--logs/--no-logs", help="Show nanobot runtime logs during retry"),
):
    """Rerun one cron job on demand for operator-confirmed recovery."""
    from loguru import logger
    from nanobot.config.paths import get_cron_dir
    from nanobot.cron.service import CronService

    config_obj = _load_runtime_config(config, workspace)
    sync_workspace_templates(config_obj.workspace_path)

    cron = CronService(get_cron_dir() / "jobs.json")

    jobs = cron.list_jobs(include_disabled=True)
    job = next((item for item in jobs if item.id == job_id), None)
    if job is None:
        console.print(f"[red]Error: Cron job not found: {job_id}[/red]")
        raise typer.Exit(1)

    runtime = build_agent_runtime(
        config_obj,
        provider_factory=_make_provider,
        cron_service=cron,
    )
    bus = runtime.bus
    agent_loop = runtime.agent

    if logs:
        logger.enable("nanobot")
    else:
        logger.disable("nanobot")

    outputs: list[str] = []

    async def on_cron_job(run_job) -> str | None:
        async def _silent(*_args, **_kwargs):
            return None

        if hasattr(agent_loop, "context") and hasattr(agent_loop.context, "build_cron_prompt"):
            prompt = agent_loop.context.build_cron_prompt(run_job.name, run_job.payload.message)
        else:
            prompt = _build_cron_execution_message(run_job.name, run_job.payload.message)
        if hasattr(agent_loop, "process_system_turn"):
            response = await agent_loop.process_system_turn(
                prompt,
                session_key=f"cron:{run_job.id}:manual",
                channel="cli",
                chat_id=f"cron-retry:{run_job.id}",
                on_progress=_silent,
                stateless=True,
                model=getattr(agent_loop, "automation_model", None),
            )
        else:
            response = await agent_loop.process_direct(
                message=prompt,
                session_key=f"cron:{run_job.id}:manual",
                channel="cli",
                chat_id=f"cron-retry:{run_job.id}",
                on_progress=_silent,
            )
        if response:
            outputs.append(response)
        return response

    cron.on_job = on_cron_job

    async def run_once() -> tuple[bool, list[str]]:
        ran = await cron.run_job(job_id, force=True)
        bus_outputs: list[str] = []
        while bus.outbound_size:
            outbound = await bus.consume_outbound()
            if outbound.content:
                bus_outputs.append(outbound.content)
        await agent_loop.close_mcp()
        return ran, bus_outputs

    console.print(f"[dim]Retrying cron job `{job.id}` ({job.name or 'unnamed'})[/dim]")
    ran, bus_outputs = asyncio.run(run_once())

    if not ran:
        console.print(f"[red]Error: Failed to rerun cron job: {job_id}[/red]")
        raise typer.Exit(1)

    rendered = bus_outputs or outputs
    if rendered:
        for item in rendered:
            _print_agent_response(item, render_markdown=markdown)
    else:
        console.print(f"[green]Retried cron job `{job.id}` successfully.[/green]")


